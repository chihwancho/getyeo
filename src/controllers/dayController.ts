// controllers/dayController.ts
import { Prisma } from '@prisma/client';
import { Response } from 'express';
import { AuthRequest, DayInput, DayResponse } from '../types';
import { AppError } from '../middleware/errorHandler';
import { prisma } from '../lib/prisma';

/** Default sort for activities on a day (matches Prisma schema: time → position → createdAt). */
const activityListOrderBy = [
  { time: 'asc' },
  { position: 'asc' },
  { createdAt: 'asc' },
] as Prisma.ActivityOrderByWithRelationInput[];

const activityNotDeletedWhere = { deletedAt: null } as Prisma.ActivityWhereInput;

// Helper function to format day response
const formatDayResponse = (day: any): DayResponse => {
  return {
    id: day.id,
    vacationId: day.vacationId,
    homestayId: day.homestayId,
    date: day.date.toISOString().split('T')[0], // YYYY-MM-DD
    notes: day.notes,
    createdAt: day.createdAt.toISOString(),
    updatedAt: day.updatedAt.toISOString(),
  };
};

// ============================================================================
// HELPER: AUTO-GENERATE DAYS FOR A VACATION
// ============================================================================

/**
 * Auto-generates Day records for a vacation from startDate to endDate.
 * Skips days that already exist.
 * Called when creating or updating a vacation.
 */
export const autoGenerateDays = async (
  vacationId: string,
  startDate: Date,
  endDate: Date
): Promise<void> => {
  const currentDate = new Date(startDate);
  
  while (currentDate <= endDate) {
    // Check if day already exists for this date
    const existingDay = await prisma.day.findUnique({
      where: {
        vacationId_date: {
          vacationId,
          date: new Date(currentDate),
        },
      },
    });

    // Create day if it doesn't exist
    if (!existingDay) {
      await prisma.day.create({
        data: {
          vacationId,
          date: new Date(currentDate),
        },
      });
    }

    // Move to next day
    currentDate.setDate(currentDate.getDate() + 1);
  }
};

// ============================================================================
// GET ALL DAYS FOR A VACATION
// ============================================================================

export const getDays = async (req: AuthRequest, res: Response) => {
  try {
    const vacationId = Array.isArray(req.params.vacationId) ? req.params.vacationId[0] : req.params.vacationId;

    if (!req.user) {
      throw new AppError(401, 'Unauthorized');
    }

    // Verify vacation exists and user owns it
    const vacation = await prisma.vacation.findUnique({
      where: { id: vacationId },
    });

    if (!vacation) {
      throw new AppError(404, 'Vacation not found');
    }

    if (vacation.userId !== req.user.id) {
      throw new AppError(403, 'You do not have access to this vacation');
    }

    // Get all days for this vacation, sorted by date
    const days = await prisma.day.findMany({
      where: { vacationId },
      orderBy: { date: 'asc' },
      include: {
        homestay: true,
        activities: {
          where: activityNotDeletedWhere, // Exclude hard-deleted activities
          orderBy: activityListOrderBy,
        },
      },
    });

    res.json(days.map(formatDayResponse));
  } catch (error) {
    if (error instanceof AppError) {
      throw error;
    }
    throw new AppError(500, 'Failed to retrieve days', { error });
  }
};

