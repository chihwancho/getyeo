// ============================================================================
// CREATE VACATION VARIANT
// ============================================================================

export const createVacationVariant = async (req: AuthRequest, res: Response) => {
  try {
    if (!req.user) {
      throw new AppError(401, 'Unauthorized');
    }

    const id = Array.isArray(req.params.id) ? req.params.id[0] : req.params.id;
    const { variant } = req.body as { variant: string };

    if (!variant) {
      throw new AppError(400, 'Variant name is required');
    }

    // Check ownership
    const parentVacation = await prisma.vacation.findUnique({
      where: { id },
      include: {
        homestays: true,
        days: {
          include: {
            activities: true,
          },
        },
      },
    });

    if (!parentVacation) {
      throw new AppError(404, 'Parent vacation not found');
    }

    if (parentVacation.userId !== req.user.id) {
      throw new AppError(403, 'You do not have access to this vacation');
    }

    // Create variant vacation (clone of parent)
    const variantVacation = await prisma.vacation.create({
      data: {
        userId: req.user.id,
        name: `${parentVacation.name} - ${variant}`,
        startDate: parentVacation.startDate,
        endDate: parentVacation.endDate,
        parentVacationId: id,
        variant,
        version: 1,
      },
      include: {
        homestays: true,
        days: {
          include: {
            activities: true,
          },
        },
      },
    });

    res.status(201).json(formatVacationResponse(variantVacation));
  } catch (error) {
    if (error instanceof AppError) {
      throw error;
    }
    throw new AppError(500, 'Failed to create vacation variant', { error });
  }
};

