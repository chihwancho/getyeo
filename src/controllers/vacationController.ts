// ============================================================================
// UPDATE VACATION
// ============================================================================

export const updateVacation = async (req: AuthRequest, res: Response) => {
  try {
    if (!req.user) {
      throw new AppError(401, 'Unauthorized');
    }

    const id = Array.isArray(req.params.id) ? req.params.id[0] : req.params.id;
    const { name, startDate, endDate } = req.body as Partial<VacationInput>;

    // Check ownership
    const vacation = await prisma.vacation.findUnique({
      where: { id },
    });

    if (!vacation) {
      throw new AppError(404, 'Vacation not found');
    }

    if (vacation.userId !== req.user.id) {
      throw new AppError(403, 'You do not have access to this vacation');
    }

    const nextStart = startDate ? new Date(startDate) : vacation.startDate;
    const nextEnd = endDate ? new Date(endDate) : vacation.endDate;

    // Validate dates if either is being changed
    if (startDate || endDate) {
      if (nextStart >= nextEnd) {
        throw new AppError(400, 'Start date must be before end date');
      }
    }

    // Update vacation
    const updated = await prisma.vacation.update({
      where: { id },
      data: {
        ...(name && { name }),
        ...(startDate && { startDate: nextStart }),
        ...(endDate && { endDate: nextEnd }),
      },
    });

    if (startDate || endDate) {
      await autoGenerateDays(id, nextStart, nextEnd);
    }

    const updatedWithDays = await prisma.vacation.findUnique({
      where: { id },
      include: {
        homestays: true,
        days: {
          orderBy: { date: 'asc' },
          include: {
            activities: true,
          },
        },
      },
    });

    if (!updatedWithDays) {
      throw new AppError(500, 'Failed to load vacation after update');
    }

    res.json(formatVacationResponse(updatedWithDays));
  } catch (error) {
    if (error instanceof AppError) {
      throw error;
    }
    throw new AppError(500, 'Failed to update vacation', { error });
  }
};

