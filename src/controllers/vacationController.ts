import { Response } from 'express';
import { AuthRequest, VacationInput, VacationResponse } from '../types';
import { AppError } from '../middleware/errorHandler';
import { prisma } from '../lib/prisma';
import { autoGenerateDays } from './dayController';

// Helper function to format vacation response
const formatVacationResponse = (vacation: any): VacationResponse => {
  const days = vacation.days?.map((day: any) => ({
    id: day.id,
    vacationId: day.vacationId,
    date: day.date.toISOString().split('T')[0],
    homestayId: day.homestayId,
    notes: day.notes,
    activities: day.activities?.map((activity: any) => ({
      id: activity.id,
      dayId: activity.dayId,
      vacationId: activity.vacationId,
      type: activity.type,
      name: activity.name,
      location: activity.location,
      coordinates: activity.coordinates,
      googlePlacesId: activity.googlePlacesId,
      time: activity.time,
      duration: activity.duration,
      timeConstraint: activity.timeConstraint,
      priority: activity.priority,
      source: activity.source,
      notes: activity.notes,
      estimatedCost: activity.estimatedCost,
      metadata: activity.metadata,
      createdAt: activity.createdAt.toISOString(),
      updatedAt: activity.updatedAt.toISOString(),
    })) || [],
    createdAt: day.createdAt.toISOString(),
    updatedAt: day.updatedAt.toISOString(),
  })) || [];

  return {
    id: vacation.id,
    userId: vacation.userId,
    name: vacation.name,
    startDate: vacation.startDate.toISOString().split('T')[0],
    endDate: vacation.endDate.toISOString().split('T')[0],
    version: vacation.version,
    parentVacationId: vacation.parentVacationId,
    variant: vacation.variant,
    homestays: vacation.homestays || [],
    days,
    createdAt: vacation.createdAt.toISOString(),
    updatedAt: vacation.updatedAt.toISOString(),
  };
};

// ============================================================================
// CREATE VACATION
// ============================================================================

export const createVacation = async (req: AuthRequest, res: Response) => {
  try {
    if (!req.user) {
      throw new AppError(401, 'Unauthorized');
    }

    const { name, startDate, endDate } = req.body as VacationInput;

    // Validation
    if (!name || !startDate || !endDate) {
      throw new AppError(400, 'Name, startDate, and endDate are required');
    }

    const start = new Date(startDate);
    const end = new Date(endDate);

    if (start >= end) {
      throw new AppError(400, 'Start date must be before end date');
    }

    // Create vacation
    const vacation = await prisma.vacation.create({
      data: {
        userId: req.user.id,
        name,
        startDate: start,
        endDate: end,
        version: 1,
      },
    });

    await autoGenerateDays(vacation.id, start, end);

    const vacationWithDays = await prisma.vacation.findUnique({
      where: { id: vacation.id },
      include: {
        homestays: true,
        days: {
          orderBy: { date: 'asc' },
          include: {
            activities: true,
          },
        },
      },
    });

    if (!vacationWithDays) {
      throw new AppError(500, 'Failed to load vacation after create');
    }

    res.status(201).json(formatVacationResponse(vacationWithDays));
  } catch (error) {
    if (error instanceof AppError) {
      throw error;
    }
    throw new AppError(500, 'Failed to create vacation', { error });
  }
};

// ============================================================================
// GET ALL VACATIONS FOR USER
// ============================================================================

export const getVacations = async (req: AuthRequest, res: Response) => {
  try {
    if (!req.user) {
      throw new AppError(401, 'Unauthorized');
    }

    const vacations = await prisma.vacation.findMany({
      where: {
        userId: req.user.id,
        parentVacationId: null, // Only get originals, not variants
      },
      include: {
        homestays: true,
        days: {
          include: {
            activities: true,
          },
        },
      },
      orderBy: {
        createdAt: 'desc',
      },
    });

    const formatted = vacations.map(formatVacationResponse);
    res.json(formatted);
  } catch (error) {
    if (error instanceof AppError) {
      throw error;
    }
    throw new AppError(500, 'Failed to fetch vacations', { error });
  }
};

